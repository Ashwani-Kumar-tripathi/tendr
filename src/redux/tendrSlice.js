import { createSlice } from '@reduxjs/toolkit'

const tendrSlice = createSlice({
  name: 'counter',
  initialState: { value: 0 },
  reducers: {
    increment: (state) => {
      state.value += 1
    },
    decrement: (state) => {
      state.value -= 1
    },
  },
})

export const { increment, decrement } = tendrSlice.actions
export default tendrSlice.reducer
